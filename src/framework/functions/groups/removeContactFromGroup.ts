import 'reflect-metadata'
import 'source-map-support/register'
import '@framework/ioc/inversify.config'
import { APIGatewayProxyEvent, Context } from 'aws-lambda'

import { RemoveContactFromGroupOperator } from '@controller/operations/groups/removeContactFromGroupOperator'
import { InputRemoveContactFromGroup } from '@controller/serializers/groups/inputRemoveContactFromGroup'
import { httpResponse } from '@framework/utility/httpResponse'
import { httpHandler } from '@framework/utility/httpHandler'
import { middyfy } from '@framework/utility/middy'
import { container } from '@shared/ioc/container'

const main = httpHandler(async (event: APIGatewayProxyEvent, context: Context) => {
  context.callbackWaitsForEmptyEventLoop = false

  const operator = container.get(RemoveContactFromGroupOperator)
  const input = new InputRemoveContactFromGroup(event.body as Object)
  const result = await operator.exec(input)

  if (result.isLeft()) {
    return httpResponse.badRequest(result.value)
  }

  return httpResponse.ok(result.value)
})

export const handler = middyfy(main)
